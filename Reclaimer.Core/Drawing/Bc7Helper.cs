    internal readonly struct BlockAttributes
    {
        public readonly byte Mode;
        public readonly byte PartitionBits;
        public readonly byte RotationBits;
        public readonly byte IndexModeBits;
        public readonly byte SubsetCount;
        public readonly byte ColourBits;
        public readonly byte AlphaBits;
        public readonly PBitMode PBitMode;
        public readonly byte Index0Bits;
        public readonly byte Index1Bits;

        public BlockAttributes(byte mode, byte partBits, byte rotBits, byte iModeBits, byte subsets, byte cBits, byte aBits, PBitMode pBits, byte i0Bits, byte i1Bits)
        {
            Mode = mode;
            PartitionBits = partBits;
            RotationBits = rotBits;
            IndexModeBits = iModeBits;
            SubsetCount = subsets;
            ColourBits = cBits;
            AlphaBits = aBits;
            PBitMode = pBits;
            Index0Bits = i0Bits;
            Index1Bits = i1Bits;
        }
    }

