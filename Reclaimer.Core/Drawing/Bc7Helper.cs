        private static readonly uint[] Weights2 = [0, 21, 43, 64];
        private static readonly uint[] Weights3 = [0, 9, 18, 27, 37, 46, 55, 64];
        private static readonly uint[] Weights4 = [0, 4, 9, 13, 17, 21, 26, 30, 34, 38, 43, 47, 51, 55, 60, 64];

        public static void InterpolatePaletteValues(int e0, int e1, int subsetIndex, int channelIndex, ref BlockAttributes blockInfo, Span<byte> palette0Bytes, Span<byte> palette1Bytes)
        {
            var (palette0Size, palette1Size) = (1 << blockInfo.Index0Bits, 1 << blockInfo.Index1Bits);
            var (weights0, weights1) = (GetWeights(blockInfo.Index0Bits), GetWeights(blockInfo.Index1Bits));

            for (var j = 0; j < palette0Size; j++)
                palette0Bytes[(subsetIndex * palette0Size + j) * 4 + channelIndex] = (byte)(((64 - weights0[j]) * e0 + weights0[j] * e1 + 32) >> 6);

            if (blockInfo.Index1Bits > 0)
            {
                for (var j = 0; j < palette1Size; j++)
                    palette1Bytes[(subsetIndex * palette1Size + j) * 4 + channelIndex] = (byte)(((64 - weights1[j]) * e0 + weights1[j] * e1 + 32) >> 6);
            }

            static uint[] GetWeights(int indexBits)
            {
                return indexBits switch
                {
                    2 => Weights2,
                    3 => Weights3,
                    _ => Weights4
                };
            }
        }
