    [Flags]
    internal enum BlockMode
    {
        Mode0 = 1,
        Mode1 = 2,
        Mode2 = 4,
        Mode3 = 8,
        Mode4 = 16,
        Mode5 = 32,
        Mode6 = 64,
        Mode7 = 128,

